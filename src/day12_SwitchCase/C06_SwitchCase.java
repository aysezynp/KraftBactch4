package day12_SwitchCase;

public class C06_SwitchCase {
    public static void main(String[] args) {
        //byte, short, string,char,int
        System.out.println("giriş");
        int sayi=3;
        switch (sayi){
            case 1:
                System.out.println("sayi 1 e eşit");
                break;
            case 2:
                System.out.println("sayi 2 ye eşit");
                 break;
            case 3:
                System.out.println("sayi 3 e eşit");
                break;
            default:
                System.out.println("sayi eşleşmedi");
        }
        System.out.println("çıkış");








    }
}
