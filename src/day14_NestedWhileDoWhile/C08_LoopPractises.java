package day14_NestedWhileDoWhile;

public class C08_LoopPractises {
    public static void main(String[] args) {
        for (int i=1;i<=10;i++){
            System.out.print(i+" ");
        }
        System.out.println(".................................");

        int j=1;
        while(j<=10){

            System.out.println(j);
            j++;
        }
        System.out.println(".................................");

        int k=1;
        do{
            System.out.println(k);
            k++;

        }while(k<=10);






    }
}
